class Usuario {
    constructor(id_usuario, nome, registro_academico, data_nascimento, email, telefone, tipo) {
        this.id_usuario = id_usuario;
        this.nome = nome;
        this.registro_academico = this.registro_academico;
        this.data_nascimento = this.data_nascimento;
        this.email = email;
        this.telefone = this.telefone;
        this.tipo = tipo;
    }
}

module.exports = Usuario;